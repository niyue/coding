package com.niyue.coding.careercup.coinchange;

/*
 * http://www.careercup.com/question?id=11585834
 * You have coins with different denominations,
 *  and you have limited number of each denominations (some maybe zero).
 *  How will you determine if you can supply the given change.
 */
public class CoinChange {
	public boolean isPossible(int[] denominations, int[] numbers, int change) {
		assert denominations.length == numbers.length && change >= 0;
		
		return false;
	}
}
